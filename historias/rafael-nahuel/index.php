                <h2 class="bold-font">Rafael Nahuel</h2>
                <p class="info light-font">El 25 de noviembre de 2017 en la ciudad bonaerense de 25 de mayo
                    fue el
                    velatorio de Santiago Maldonado, el joven encontrado muerto en el río Chubut 78 días después de
                    haber desaparecido tras un operativo represivo de Gendarmería contra mapuches, en la provincia de
                    Chubut. Ese mismo día un integrante de la comunidad Lafken Winkul Mapu era asesinado en Río Negro,
                    como consecuencia de un disparo por la espalda realizado por un integrante del grupo Albatros de la
                    Prefectura Naval.<br />
                    Rafael Nahuel tenía 22 años. No era militante en la lucha mapuche aunque la rama paterna de su
                    familia sí. Hacía changas para aportar a la casa. En el Semillero del Colectivo Al Margen aprendía
                    el oficio de carpintería. Las distintas intervenciones de las fuerzas federales que terminaron con
                    su muerte comenzaron el 23 de noviembre cuando efectivos ingresaron a las tierras que habían sido
                    ocupadas dos meses antes por integrantes de la comunidad Lafken Winkul Mapu, cerca del lago
                    Mascardi, en el Parque Nacional Nahuel Huapi. Con orden del juez federal Gustavo Villanueva,
                    efectivos de la Policía Federal y de Prefectura detuvieron a varies mapuches, entre elles mujeres y
                    niñes que estuvieron precintades durante varias horas. Algunes pudieron escapar hacia las montañas.
                    En el contexto de estas persecuciones, el 25 de noviembre efectivos del grupo Albatros de la
                    Prefectura dispararon con balas de plomo contra un grupo que se encontraba refugiado: mataron Rafael
                    Nahuel e hirieron a otras cuatro personas.<br />
                    Desde un comienzo, el gobierno nacional optó por hacer propia la versión de Prefectura: intentaron
                    instalar que los agentes fueron atacados por un grupo armado. Alimentaron la teoría de un supuesto
                    enfrentamiento. A su vez, la reacción oficial, similar a lo sucedido con Santiago Maldonado, fue
                    anunciar que se abriría un sumario interno sobre los integrantes de Prefectura y no separarían de su
                    puesto a ninguno de los involucrados.<br />
                    Fausto Jones Huala, hermano del líder mapuche Facundo Jones Huala, y Lautaro Alejandro González
                    fueron quienes bajaron al joven moribundo hasta la base de la montaña. Los dos quedaron
                    inmediatamente detenidos tras auxiliar al chico. Los prefectos quisieron incriminarlos, pero las
                    pericias que les hicieron demostraron que ellos no tenían rastros de pólvora en las manos. La
                    Asamblea Permanente de Derechos Humanos (APDH) e integrantes de la comunidad Lafken Wuinkuñ Mapu
                    aseguraron, desde un comienzo, que los mapuches no estaban armados, que respondieron con piedras y
                    que los prefectos “los corrieron a tiros”.<br />
                    Tras el asesinato de Rafael Nahuel se hicieron marchas en Bariloche para exigir justicia. Aunque
                    también hubo movilizaciones en apoyo a la Prefectura. El CELS presentó un pedido de informes al
                    Ministerio de Seguridad de la Nación para que explicara e hiciera públicos los motivos por los
                    cuales había involucrado a este grupo Albatros, de rasgos militarizados, para intervenir en un
                    conflicto social.<br />
                    El asesinato y su investigación se dieron en el medio de un contexto de estigmatización y
                    persecución del pueblo mapuche. En febrero de 2018 integrantes del Centro Atómico Bariloche tuvieron
                    que salir a desmentir rumores sobre las pericias: el diario Clarín llegó a publicar que habían
                    encontrado restos de pólvora en las manos del chico asesinado.<br />
                    En enero de 2019 el juez federal, Leónidas Moldes, procesó a cinco integrantes del grupo Albatros:
                    Juan Obregón, Sergio García, Sergio Cavia, Carlos Sosa y Francisco Javier Pintos. Moldes los acusó
                    del delito de "homicidio agravado en legítima defensa". Replicó, así, la versión de los prefectos y
                    del gobierno nacional a pesar de que no había indicios de que los mapuches tuvieran armas. También
                    procesó a los muchachos que trasladaron el cuerpo. Los acusó de usurpación y atentado a la
                    autoridad.<br />
                    Más tarde, la Cámara de Roca procesó a Pintos por “homicidio agravado”. Desde el comienzo de la
                    investigación él fue señalado como el autor material del crimen. La Cámara también le dictó prisión
                    preventiva y otorgó la falta de mérito a los demás prefectos. Sin embargo, Pintos terminó en
                    libertad tras la aparición del peritaje de Gendarmería a su favor.<br />
                    En febrero de 2020, la Cámara Federal de Apelaciones de Roca determinó la falta de mérito del
                    prefecto para Pintos. Los camaristas argumentaron su decisión al señalar que había "dos pericias
                    absolutamente contradictorias entre sí".
