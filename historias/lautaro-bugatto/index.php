                <h2 class="bold-font">Lautaro Bugatto</h2>
                <p class="info light-font">Durante 2012 el “estado policial” y la obligación de portar
                    armas de las
                    fuerzas de seguridad fueron un tema de la agenda mediática. Se logró colocar en la conversación
                    pública que el uso de armas por parte de policías fuera de servicio tiene alta incidencia en las
                    muertes violentas. El Movimiento Evita, a través del diputado Leonardo Grosso, activó la “Campaña
                    Nacional contra la Violencia Institucional” que articula diversos actores que enfrentan este tipo de
                    violencia desde la denuncia, la organización social y política en los barrios y el litigio como
                    parte de la lucha por el reconocimiento de derechos.<br />
                    A diferencia de la mayoría de las víctimas de los hechos de violencia policial que suelen ser
                    jóvenes varones pobres, Lautaro era un futbolista de clase media que estaba a punto de debutar en la
                    primera división del fútbol argentino en Banfield. Además, venía de una familia ligada a un
                    movimiento social con anclaje territorial. Su hermano, Gonzalo, se puso al frente de la búsqueda de
                    justicia con tenacidad. Su historia llegó a los medios de comunicación y se pudo frenar la
                    propagación de la versión policial.<br />
                    La madrugada del sábado 6 de mayo de 2012 Lautaro estaba en la puerta de su casa en Burzaco, en la
                    zona sur del conurbano bonaerense, cuando el policía bonaerense David Benítez le disparó. El joven
                    de 20 años estaba por subir a su auto para salir a bailar con sus amigos. Benítez estaba fuera de
                    servicio e intentaba evitar a los tiros el robo de un ciclomotor en el que circulaban su hermana y
                    su hija. Gatilló, al menos, 7 veces su arma reglamentaria sin dar la voz de alto e hirió gravemente
                    en el pulmón a Lautaro, quien murió minutos después en un sanatorio de la zona. Las balas también
                    alcanzaron a uno de los presuntos ladrones.<br />
                    “Nadie le quería decir a mi mamá que mi hermano había fallecido. No te imaginás el peso que fue
                    tener que decirle que lo habían matado. Fue y es tremendo”, dice Gonzalo Bugatto, años
                    después.<br />
                    Benítez intentó involucrar a Lautaro en el robo que el policía perseguía y hasta llegó a decir que
                    se enfrentó a los tiros con el joven. “¿Cómo mi hermano iba a salir a robar en la puerta de su casa
                    si estaba por debutar en primera? Poner la carga de la responsabilidad en la víctima fue algo vil”,
                    dice el hermano de Lautaro, que lo tiene tatuado en su cuerpo pateando una pelota.<br />
                    A su vez, los policías encargados de la investigación desplegaron las maniobras habituales de
                    encubrimiento de la Bonaerense: no se preservó la escena del crimen, no se tuvieron en custodia
                    elementos de prueba, realizaron reuniones informales con el imputado y sus familiares que eran
                    testigos del hecho.<br />
                    A las pocas horas del crimen, el por entonces gobernador de la provincia, Daniel Scioli, y el
                    ministro de Justicia y Seguridad, Ricardo Casal, se solidarizaron con los familiares de Lautaro y
                    condenaron la actuación del agente, aunque lo calificaron como un caso anómalo que no respondía a su
                    modelo de policía ni dispusieron ninguna medida para garantizar el esclarecimiento del episodio. El
                    CELS representó a la familia de Lautaro en una acción administrativa ante la Auditoría de Asuntos
                    Internos del Ministerio de Justicia y Seguridad bonaerense, que dio por probadas algunas de las
                    irregularidades cometidas por la Policía de la provincia de Buenos Aires e inició la investigación
                    de otras líneas denunciadas.<br />
                    El policía primero fue imputado por homicidio simple. Después, la Sala I de la Cámara de Apelaciones
                    de Lomas de Zamora confirmó la acusación, pero modificó la calificación porque entendió que se había
                    tratado de un exceso en la legítima defensa del policía.<br />
                    Dos años y tres meses después del crimen, en septiembre de 2014, el Tribunal Oral en lo Criminal Nº
                    10 de Lomas de Zamora condenó a David Ramón Benítez, de 34 años, por el delito de “homicidio
                    agravado por el uso de arma de fuego” a 14 años de prisión. Además, les jueces Daniel Manzini,
                    Susana Silvestrini y José Ignacio Polizza pidieron que se investigara si la familia del policía
                    había incurrido en falso testimonio.
                </p>
