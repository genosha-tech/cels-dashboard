                <h2 class="bold-font">La Masacre de San Miguel del Monte</h2>
                <p class="info light-font">Una de las costumbres de los pibes y las pibas de San Miguel
                        del Monte es
                        dar la vuelta en auto a los 12
                        kilómetros que rodean a la laguna del pueblo. Aníbal Suárez (22 años), Gonzalo Domínguez (14
                        años), Danilo Sansone (13 años),
                        Camila López (13 años) y Rocío Guagliarello (13 años) habían salido el 20 de mayo de 2019 a
                        replicar el ritual. Aníbal manejaba
                        el Fiat Spazio en el que viajaban. Iban escuchando música cuando patrulleros de la policía
                        Bonaerense comenzaron a perseguirlos
                        a los tiros. La única que sobrevivió a la persecución fue Rocío.<br />
                        Rodrigo se convirtió en testigo de la persecución fatal. El joven de 18 años caminaba por la
                        colectora 9 de julio,
                        a la orilla de la Ruta 3. Escuchó disparos y vio un Fiat 147 color gris claro que se estrellaba
                        contra un camión
                        acoplado estacionado. Rodrigo grabó todo con su celular. Fue uno de los principales registros de
                        la masacre que la
                        Bonaerense y la Municipalidad quisieron ocultar. Y Rodrigo fue uno de los primeros en ir a
                        declarar en sede policial
                        pero los agentes le cambiaron su testimonio: escribieron “estruendos” en lugar de
                        “disparos”.<br />
                        No fue un accidente, como circuló en un comienzo en los medios que se hicieron eco de la versión
                        policial.
                        Gran parte del pueblo de Monte se movilizó para desarticular la mentira que quisieron contar los
                        agentes:
                        les amigues difundieron en redes sociales lo que había pasado, vecinos y vecinas se ofrecieron
                        como testigos
                        y aportaron pruebas y registros. También hicieron movilizaciones multitudinarias a la comisaría
                        y cortes de ruta:
                        el caso se nacionalizó.<br />
                        Alexis fue otra pieza clave para desarmar la teoría del “accidente”. El joven trabajaba en el
                        Centro de Monitoreo de Monte.
                        Revisó las cámaras de seguridad de ese día y dio con la imagen de uno de los policías, con medio
                        cuerpo afuera del patrullero,
                        disparando al auto en el que iban les chiques. Hizo una captura y la envió por WhatsApp a una
                        vecina. El video se viralizó
                        y a Alexis lo castigaron con una suspensión.<br />
                        La Bonaerense venía hostigando a algunos de los chicos que iban en el auto desde antes de la
                        persecución. Aníbal, el conductor,
                        se había comprado el coche hacía dos meses y todavía no tenía todos los papeles en regla. Estaba
                        juntando la plata para hacer
                        la transferencia. Un mes antes lo habían parado en un control. Como no tenía el auto a su nombre
                        lo llevaron a la estación
                        de policía comunal de Monte. Le dijeron que la multa era de 35 mil pesos pero que podía irse si
                        les pagaba 5 mil. Se los pagó
                        al comisario a cambio de su libertad.<br />
                        En los registros de la estación de policía de Monte quedó asentada la detención de Danilo en
                        marzo de 2019 por “daño”.
                        Estaba en la plaza rapeando cuando se lo llevaron sin ningún motivo junto con dos chicos más.
                        Tenía 13 años y su
                        detención fue ilegal.<br />
                        La Comisión provincial por la Memoria (CPM) se convirtió en patrocinadora de los familiares de
                        Camila y Danilo.
                        Dos abogades representan a Rocío y a las familias de Gonzalo y Aníbal. El CELS intervino en
                        septiembre de 2019
                        como querellante institucional, brinda asistencia psicoterapéutica a una de las familias y
                        acompaña a los equipos
                        de atención que ofrecen asistencia individual y grupal a las víctimas.
                        Tras las muertes, el por entonces ministro de Seguridad de la provincia, Cristian Ritondo, habló
                        de policías que
                        "han manchado el uniforme y han deshonrado a sus compañeros".<br />
                        En junio de 2019 el poder judicial platense dictó prisión preventiva para 11 de les 13 policías
                        detenides.
                        Luego, el juez de Garantías Eduardo Luis Silva Pelossi procesó a 10 policías y al secretario de
                        Seguridad local
                        por la responsabilidad en los asesinatos.<br />
                        Los policías que dispararon -Manuel Monreal, Rubén Alberto García, Leonardo Daniel Ecilape y 
                        Mariano Alejandro Ibáñez- fueron imputados por el homicidio agravado de les 4 chiques y 
                        el intento de asesinato de Rocío.<br />
                        En septiembre, el fiscal Lisandro Damonte pidió que se abra una investigación paralela por
                        "incumplimiento de los deberes
                        de funcionario público" contra la intendenta de esa ciudad, Sandra Mayol. La medida había sido
                        pedida por la CPM.<br />
                        En abril de 2020, el Ministerio Público bonaerense cerró la investigación que tiene como
                        acusados a veinte policías,
                        el secretario de Seguridad de la Municipalidad de San Miguel del Monte y tres peritos de la
                        subdelegación Cañuelas
                        de la Policía Científica. Las familias y el pueblo de Monte esperan el juicio para saber por qué
                        los Bonaerenses los
                        persiguieron esa noche y dispararon contra un auto en el que iban adolescentes.</p>
