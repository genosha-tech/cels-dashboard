                <h2 class="bold-font">Facundo Ferreira</h2>
                <p class="info light-font">Facundo Ferreira quería empezar la secundaria, jugar a la pelota
                    y hacer
                    feliz a su abuela. Tenía 12 años. La madrugada del 8 de marzo de 2018 policías de la División
                    Motoristas del Servicio 911 de la policía tucumana lo persiguieron a él y a un amigo. A Facundo lo
                    mataron de un tiro en la nuca. Fue en la capital de Tucumán. Los chicos se habían escapado para ver
                    las carreras de motos que se corren cerca del Parque 9 de Julio. Una travesura que terminó con
                    Facundo muerto. El nene le había pedido permiso a su abuela para salir a comprar un sandwich.<br />
                    Los policías dispararon doce balas: 11 de plomo y una de goma. “A mi amigo lo mataron los policías”,
                    dijo Juan, el adolescente de 14 años que manejaba la moto. Uno de los tiros le impactó en la
                    pierna.<br />
                    La versión policial apuntó contra los chicos. Quisieron instalar que estaban armados y que los
                    agentes se defendieron de una agresión sin saber que eran niños. Los efectivos fueron liberados el
                    mismo día del crimen.<br />
                    La causa judicial y la cobertura mediática del caso fueron un camino de reveses hasta poder desarmar
                    la ficción que querían instalar los policías. También fueron una lucha contra la estigmatización del
                    niño en los medios de comunicación: ¿qué hacía en ese lugar de noche? ¿Por qué su familia le había
                    permitido salir? Todo esto en un contexto donde las autoridades políticas nacionales avalaban casi a
                    diario que las fuerzas de seguridad dispararan preventivamente y recibían a policías que habían
                    matado en la Casa Rosada y el Ministerio de Seguridad. Patricia Bullrich, por entonces ministra de
                    esa cartera, en declaraciones a la prensa defendió a los policías que gatillaron contra Facundo.
                    “Disparar o no por la espalda depende de la situación” y “es un detalle”, dijo la funcionaria, quien
                    además acusó a la víctima de “disparar contra la policía” cuando los resultados de los peritajes aún
                    no habían determinado que eso hubiera ocurrido.<br />
                    Las primeras pericias demostraron que las vainas recolectadas habían sido disparadas solo por los
                    policías. Al comienzo, la prueba de dermotest, que evidencia la presencia de parafina, dio positivo
                    en una de las manos de Facundo y en las dos manos del adolescente que manejaba la moto. Los abogados
                    que representaban a la familia de Facundo reclamaron una contraprueba ya que durante varias horas la
                    policía tucumana había tenido vía libre para manipular la escena del crimen. Fue entonces cuando
                    otro estudio más preciso demostró que el chico no tenía rastros de fulminantes en sus manos.<br />
                    La investigación judicial develó que los policías demoraron en ayudar al chico moribundo: el
                    asesinato ocurrió a la 1.23 de la madrugada pero tardaron en llevarlo al Hospital Padilla y recién a
                    las 4.30 avisaron a la familia. En la guardia del hospital entró como si hubiese sufrido un
                    accidente de tránsito.<br />
                    Los resultados toxicológicos de los efectivos confirmaron que uno de los policías había consumido
                    cocaína y marihuana. Nicolás Montes de Oca y Mario Díaz Cáceres, quedaron imputados por "homicidio
                    agravado", en octubre de 2018 el juez Facundo Maggio ordenó la detención de Montes de Oca. El
                    policía de 24 años había sido aprehendido a fines de septiembre acusado de robarle a una mujer.
                    Hasta ese momento, el agente estaba en actividad y cumplía funciones administrativas. Los castigos
                    se activaron cuando se lo encontró sospechoso de un robo y no del asesinato de un niño. La policía
                    de Tucumán lo pasó a la condición de "pasivo por proceso" y le abrió un sumario administrativo tras
                    el robo. Todavía está pendiente el debate oral y público por el crimen.<br />
                    Las tías y la abuela de Facundo asumieron la militancia por el reclamo de Justicia. Están
                    organizadas con otres familiares. En estos años recibieron amenazas por parte de les familiares de
                    los policías.<br />
                    Desde un comienzo, la organización Andhes (Asociación de abogados y abogadas del Noroeste argentino)
                    acompañó a la familia Ferreira y trabajó para hacer visible la muerte de Facundo en los medios. “Si
                    no fuera por los abogados de Andhes, el caso iba a quedar en la nada porque no íbamos a salir a la
                    calle. Nos hicieron sentir protegidos”, dice Malvina Ferreira, una de las tías de Facundo. Además de
                    sostener la movilización callejera, la conversación mediática y representar a la familia en la causa
                    judicial, lograron llevar el caso a instancias internacionales. En mayo de 2018 el Comité de la ONU
                    sobre los Derechos del Niño expresó durante una audiencia su “preocupación por la situación de la
                    violencia policial contra niñas, niños y adolescentes” en Argentina.
