                <h2 class="bold-font">Santiago Maldonado</h2>
                <p class="info light-font">A Santiago Maldonado lo apodaban El Brujo y en su 25 de Mayo
                    natal lo
                    conocían como Lechuga. Era tatuador, artesano, músico y anarquista. Los últimos diez años de su vida
                    los pasó viajando por Argentina, Uruguay, Paraguay, Brasil y Chile. Durante 77 días estuvo
                    desaparecido. Murió a los 28 años mientras participaba de una protesta.<br />
                    El 1 de agosto de 2017 estuvo en un corte de ruta para defender el territorio mapuche en la Pu Lof
                    en Resistencia de Cushamen. Gendarmería tenía orden de la justicia federal para despejar la ruta
                    nacional N°40 y lo hizo con un operativo violento. Cuando les manifestantes, entre quienes estaba
                    Santiago Maldonado, se replegaron en el territorio donde vive la comunidad, más de 50 gendarmes los
                    persiguieron y avanzaron sobre el predio sin autorización judicial. Allanaron viviendas, quemaron
                    pertenencias de las familias y secuestraron teléfonos, herramientas de trabajo y libros. Usaron el
                    argumento de la “flagrancia”: en los días previos, el por entonces jefe de Gabinete del Ministerio
                    de Seguridad de la Nación, Pablo Noceti, había dicho que utilizarían esa figura para detener a les
                    miembres de esta comunidad, con quienes –según él– no había nada que hablar.<br />
                    Por fuera de los protocolos de actuación, esa mañana, los agentes llevaban hachas, dispararon balas
                    de goma y arrojaron piedras. Una vez que el operativo ilegal y violento terminó, después de cinco
                    horas, la comunidad se dio cuenta que faltaba el joven a quien conocían como el Brujo.<br />
                    La comunidad y la familia denunciaron su desaparición. Creían que podía haber sido detenido. El
                    CELS, junto al resto de los organismos de derechos humanos, acompañó la visibilización de la
                    denuncia e intentó que las dependencias oficiales lo buscaran. Las historias de otras desapariciones
                    dan cuenta de que sin presión social y sin insistencia sobre les funcionaries no hay una iniciativa
                    estatal de búsqueda inmediata y sistemática.<br />
                    La pregunta “¿Dónde está Santiago Maldonado?” recorrió el mundo. Su rostro barbudo se volvió
                    pancarta y la necesidad de una respuesta al interrogante empujó múltiples movilizaciones. La
                    estrategia del gobierno del expresidente Mauricio Macri fue intentar desarmar la solidaridad que
                    provocó la desaparición del joven. El Estado destinó sus recursos a atacar a su familia, a la
                    comunidad y a quienes los acompañaban. El Poder Ejecutivo Nacional planteó hipótesis infundadas que
                    buscaban instalar que él no estaba en el lugar, que estaba ausente por propia voluntad o que había
                    fallecido antes de la represión. En segundo lugar, las autoridades ocultaron cómo había actuado la
                    Gendarmería en el operativo. En tercer lugar, el Estado no lo buscó de manera eficiente. Por su
                    parte, el Poder Judicial, con una historia conflictiva con la comunidad Pu Lof, demoró en activar
                    tanto la búsqueda como la investigación de las posibles responsabilidades de los gendarmes.<br />
                    Finalmente, el 17 de octubre de 2017, durante un rastrillaje en el río Chubut, un equipo de buzos
                    tácticos de Prefectura, lo encontró. Tras el hallazgo, algunos funcionarios del gobierno nacional se
                    dedicaron a instalar una nueva conjetura que le quitara responsabilidad a la Gendarmería y las
                    responsabilidades estatales. Buscaban el cierre del caso.<br />
                    A fines de 2018, el juez federal Gustavo Lleral cerró la causa y desligó de responsabilidades
                    penales a todos los funcionarios públicos que intervinieron en la represión de la protesta de la Pul
                    Lof en Chubut. En septiembre de 2019, la Cámara Federal de Comodoro Rivadavia ordenó reabrir la
                    causa, realizar algunas medidas de prueba solicitadas por las querellas e investigar las
                    circunstancias de la muerte. Esto fue confirmado por la Cámara Federal de Casación Penal, que
                    también consideró que no podía desligarse la muerte del operativo de la Gendarmería, y que por lo
                    tanto todo debe ser investigado en conjunto.<br />
                    Desde el día en el que la comunidad cortó la Ruta 40 se abrieron cinco causas judiciales alrededor
                    del operativo. El primer expediente es contra los propios manifestantes. En esa causa Otranto
                    procesó a dos mapuche por corte de ruta, resistencia y desobediencia a la autoridad, daños, amenazas
                    y lesiones graves a dos gendarmes. El habeas corpus presentado por la Comisión Provincial por la
                    Memoria (CPM) el 2 de agosto de 2017 inició la búsqueda de Santiago Maldonado. Tres días después,
                    ante la sospechas de la actuación de las fuerzas de seguridad, se abrió una causa por “averiguación
                    de delito”. El 24 de agosto la fiscal formalizó el reclamo de la familia y los organismos de
                    derechos humanos: la carátula del expediente 8232/2017 pasó a ser “desaparición forzada”. La cuarta
                    causa está en Comodoro Py. A partir de la denuncia de la legisladora porteña Myriam Bregman, el juez
                    Daniel Rafecas investiga a Gendarmería y al Ministerio de Seguridad por tareas de espionaje ilegal
                    contra la familia Maldonado y organizaciones sociales.<br />
                    La última investigación judicial se inició a partir de la denuncia del Ministerio de Seguridad de la
                    Nación en enero de 2018 contra siete integrantes de la comunidad y un amigo de Santiago por “falso
                    testimonio” por sus declaraciones en la causa de desaparición forzada.<br />
                    Una paradoja: de cinco expedientes que tramitan en la justicia federal, con una persona muerta que
                    el Estado no pudo encontrar durante casi tres meses después de un violento operativo, los únicos
                    procesados son dos mapuche. Hasta ahora ningún gendarme fue ni siquiera sancionado por las
                    ilegalidades del operativo, como ingresar sin orden judicial, arrojar piedras a les manifestantes o
                    hacer una hoguera con las pertenencias de la comunidad.<br />
                    El 30 de julio de 2020, al cumplirse 3 años de la desaparición y muerte de Santiago Maldonado, la
                    Comisión Interamericana de Derechos Humanos (CIDH) pidió al Estado argentino que dé explicaciones en
                    el plazo de tres meses. El 3 de agosto las nuevas autoridades del Ministerio de Seguridad de la
                    Nación, a cargo de Sabina Frederic denunciaron a Pablo Nocetti, al ex director de Gendarmería y a su
                    subdirector por el operativo represivo que terminó con el joven muerto. La familia de Santiago sigue
                    sin tener una respuesta del Estado.

