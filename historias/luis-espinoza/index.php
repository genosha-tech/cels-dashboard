                <h2 class="bold-font">Luis Espinoza</h2>
                <p class="info light-font">Luis Armando Espinoza se convirtió en un desaparecido el 15 de
                    mayo de 2020.
                    Era un trabajador rural de 32 años, criado en Rodeo Grande, un pequeño pueblo de 650 habitantes del
                    sudeste tucumano. Ante su ausencia, la familia exigió que las autoridades lo buscaran y, gracias al
                    acompañamiento de las organizaciones de derechos humanos y la atención periodística, lograron que la
                    búsqueda tuviera repercusión nacional. Su historia se convirtió en un caso emblemático de la
                    violencia institucional en tiempos de cumplimiento del Aislamiento Social Preventivo y Obligatorio
                    (ASPO).<br />
                    El operativo que terminó con el asesinato y la desaparición de Luis Espinoza comenzó como un
                    procedimiento policial en el marco del cumplimiento de las medidas que impuso la pandemia global por
                    el coronavirus. Aquella mañana del viernes 15 de mayo el subcomisario Rubén Héctor Montenegro, a
                    cargo de la dependencia policial de Monteagudo, buscaba impedir una carrera cuadrera, como les
                    llaman a las competiciones de caballos en la zona de El Melcho. Juntó a los agentes que estaban de
                    guardia y mandó a llamar a otros tres. También convocó al operativo al empleado de seguridad de la
                    comuna. Los nueve policías y el agente comunal salieron en tres autos particulares sin uniforme ni
                    identificación. El patrullero quedó estacionado en la comisaría. El grupo de policías llegó al lugar
                    a los tiros. Al escuchar los disparos, unos jinetes que se habían acercado para las carreras se
                    dispersaron.<br />
                    A esa misma hora, los hermanos Luis Armando y Juan Antonio Espinoza cabalgaban por un camino vecinal
                    cercano hacia Rodeo Grande. Cuando oyeron los tiros intentaron acelerar el paso y se metieron en una
                    tranquera. Luis iba un poco más adelante que Juan. Los policías de la moto los vieron y avisaron a
                    sus compañeros para que se acercaran. Hubo más disparos policiales con distintas armas de fuego. La
                    yegua que montaba Juan Antonio Espinoza se asustó y salió al galope. Juan Antonio quedó en el suelo
                    desmayado. Luis se acercó para ver a su hermano y el oficial auxiliar José Alberto Morales le
                    disparó por la espalda. Cuando Juan se despertó, recuperado del golpe en la cabeza por la caída,
                    Luis ya no estaba.<br />
                    Después de una semana de búsqueda, a Luis Espinoza lo encontraron en la provincia de Catamarca
                    atravesado por un tiro en la espalda que salió de un arma reglamentaria de la policía tucumana. Los
                    investigadores llegaron hasta allí por la confesión de uno de los policías que había participado del
                    operativo.<br />
                    El pacto de silencio entre los agentes para mantener oculta la verdad sobre el asesinato de Luis se
                    rompió cuando uno de ellos se quebró ante la fiscal que investigaba su desaparición. Habían cargado
                    el cadáver en uno de los autos y, una vez en la provincia vecina, lo habían arrojado desde un
                    precipicio. La autopsia que se hizo en la Morgue de San Miguel de Tucumán indicó que hubo un disparo
                    de arma de fuego de una 9 milímetros que perforó el pulmón y lesionó la zona intercostal.
                    Nueve policías y un empleado comunal están detenidos por los delitos de desaparición forzada seguida
                    de muerte y privación ilegítima de la libertad de Luis Espinoza. Dos de los agentes tenían
                    antecedentes por violencia policial: se trata de Claudio Zelaya y Eduardo González Rojas, que habían
                    sido acusados de golpear a Alan Andrada en la comisaría de la ciudad de Simoca en octubre de 2018,
                    quien finalmente falleció días después producto de una hemorragia por los golpes recibidos.<br />
                    La familia de Luis, junto con los organismos de derechos humanos locales como la Asamblea Permanente
                    por los Derechos Humanos (APDH), HIJOS y Abogadas y Abogados del noroeste argentino en Derechos
                    Humanos y Estudios Sociales (ANDHES), realizaron manifestaciones para visibilizar la historia y
                    cercar la impunidad. La APDH, a su vez, presentó ante el titular de la Oficina Regional para América
                    del Sur del Alto Comisionado de las Naciones Unidas para los Derechos Humanos (Acnudh), Ignacio
                    Rocangliolo, la historia del peón rural. Esposo de María Soledad Ruiz, padre de seis hijos, hermano
                    de siete varones y siete mujeres, hijo de doña Gladys y don Filomeno. Luis era el sostén económico
                    de una familia numerosa. Entre los pedidos de justicia también está el reclamo de reparación por
                    parte del Estado para que el crimen no profundice la situación de pobreza de su familia.
                </p>
