                <h2 class="bold-font">Lucas Cabello</h2>
                <p class="info light-font">Lucas Cabello sobrevivió a la violencia policial cuando tenía 20
                    años. Tres
                    balazos atravesaron su cuerpo el 9 de noviembre de 2015, en el barrio porteño de La Boca, cuando
                    volvía de comprar el almuerzo para él, su pareja y su hija de dos años. Un tiro entró por la
                    mandíbula y se alojó en la médula ósea del joven; los otros dos, que fueron producidos cuando ya
                    estaba en el piso, le perforaron el bazo y un testículo. Los tres disparos salieron del arma de
                    Ricardo Ayala, oficial de la Comuna 4 de la Policía Metropolitana y fueron ejecutados a corta
                    distancia.<br />
                    Las huellas de la violencia policial todavía hoy quedan en el cuerpo de Lucas: convive con una
                    hemiplejia irreversible que lo obliga a moverse en silla de ruedas. Antes del ataque policial, había
                    trabajado como albañil, mecánico de autos y pizzero. En el último tiempo estaba trabajando como
                    cuidacoches frente al restaurante Il Materello, a la vuelta de su casa. Después de la balacera,
                    estuvo casi dos años internado. En los primeros tres meses no pudo comer ni beber por su propia
                    cuenta.<br />
                    A fines de 2016 el policía que le disparó fue procesado por el delito de homicidio doloso en grado
                    de tentativa agravado por su condición de miembro de una fuerza de seguridad. El camino judicial
                    para cercar a la impunidad tuvo sus obstáculos: el juez de primera instancia Osvaldo Rappa había
                    considerado el hecho un "altercado" y le imputó a Ayala "exceso en la legítima defensa" y por eso lo
                    dejó en libertad.<br />
                    Cuatro años después el agente seguía cumpliendo funciones administrativas en la Policía de la
                    Ciudad. El comienzo del debate oral se postergó dos veces durante 2019 hasta que el 5 de diciembre
                    de ese año el Tribunal Oral Criminal 1 condenó a Ricardo Ayala a 16 años de cárcel por “intento de
                    homicidio agravado”. Además, no podrá volver a integrar una fuerza policial por 19 años. El fallo
                    fue unánime y llevó la firma de los jueces Adrián Pérez Lance, Fernando Ramírez y Luis Salas, que
                    rechazaron el pedido de absolución planteado por los abogados defensores. A su vez, ordenaron que se
                    investiguen todas las trabas, encubrimientos y violaciones a los deberes de funcionario público que
                    se habrían cometido para tratar de desviar la acusación contra Ayala.<br />
                    Cuando se produjeron los hechos, desde la Policía Metropolitana difundieron que había sido un
                    tiroteo, pero los testigos sostuvieron desde un primer momento que el joven no estaba armado y que
                    vieron cómo Ayala disparó dos veces mientras la víctima estaba en el piso. Lo único que llevaba el
                    joven en sus manos eran sandwiches. La pericia balística demostró que las tres vainas encontradas en
                    la escena del crimen eran del arma reglamentaria del policía.<br />
                    Por otra parte, la versión oficial -difundida por María Eugenia Vidal, entonces vicejefa de Gobierno
                    de la ciudad y su ministro de Justicia y Seguridad de la Ciudad de Buenos Aires, Guillermo
                    Montenegro– sostuvo que el agente había intervenido porque estaba ocurriendo una situación de
                    violencia de género y en esas circunstancias Lucas Cabello lo apuntó con un arma. Sin embargo, como
                    se confirmó después, el chico no era la persona sobre la que pesaba una restricción judicial por
                    violencia contra una mujer y no se encontraron indicios de que estuviera armado. En un primer
                    momento, Ayala quedó detenido en la Comisaría 24 de la Policía Federal Argentina (PFA) y tomó
                    intervención la Procuraduría de Violencia Institucional (PROCUVIN). Les abogades Gabriela Carpinetti
                    y Nahuel Berguier acompañaron a Lucas y Carolina Vila, su mamá, en el desarrollo de estrategias
                    judiciales y también en la visibilidad del caso con conferencias de prensa e intervenciones en los
                    medios para derribar la versión oficial.<br />
                    “Las primeras audiencias fueron de nervios e impotencia. Ver al policía que trató de matarme sentado
                    ahí esperando era difícil. El acompañamiento de mi familia y las agrupaciones del barrio en todas
                    las audiencias fueron muy importantes porque estuvieron al lado mío siempre. Me daban tranquilidad.
                    Yo fui a contar mi verdad y los jueces me dieron la razón”, dice Lucas, meses después del juicio que
                    condenó al policía que le disparó.

