                <h2 class="bold-font">Patricio Barros Cisneros</h2>
                <p class="info light-font">El sábado 28 de enero 2012 hacía mucho calor y Patricio Barros
                    Cisneros
                    esperaba, en el encierro, la visita de su novia, Giselle. Ella estaba embarazada de cuatro meses. Él
                    estaba cumpliendo una condena por robo en la Unidad 46, en el partido de San Martín, que depende del
                    Servicio Penitenciario Bonaerense (SPB). Él tenía 26 años, ella era más chica: tenía 18. Patricio
                    pidió que el encuentro con su novia fuera en una pieza. No quería ir al patio porque ahí tenían que
                    estar al sol y ella ya había esperado tres horas para verlo.<br />
                    Primero se lo pidió a los penitenciarios Rodrigo Emilio Chaparro y a Gerardo Rodolfo Luna. Le
                    dijeron que no y lo llevaron con el guardia a cargo, Héctor Aníbal Mario, que insistió con la
                    negativa. Patricio se enojó y los insultó. Ellos le respondieron de una manera desproporcionada: con
                    golpes, patadas, trompadas y gas pimienta. “Ya basta. Ya fue”, gritaba él esposado en el piso y a la
                    vista de todos en uno de los pasillos del penal. Lo vieron otros presos que trabajaban en la cocina,
                    los internados en Sanidad y quienes esperaban para ingresar al SUM de visitas.<br />
                    Uno de los guardiacárceles le saltó encima mientras estaba tirado. Fue entonces cuando dejaron de
                    escuchar la voz de Patricio. Las pericias indicaron que murió asfixiado. El forense contó más de 30
                    golpes en todo su cuerpo.<br />
                    Después del crimen, los penitenciarios y las autoridades del penal activaron la maniobra de
                    encubrimiento: le dijeron a Giselle que su novio había atacado a los agentes y después se había
                    golpeado él mismo contra las rejas. Ella no les creyó.<br />
                    La autopsia demostró que tenía golpes “apergaminados”, que son los que se producen durante el
                    proceso de la muerte. No se pueden autoinfligir. Los guardiacárceles hicieron todo para montar su
                    ficción: uno de ellos se clavó una faca en la pierna para simular que el joven lo había
                    atacado.<br />
                    Dos días después, abogados de los organismos de derechos humanos y Juan Manuel Casolati -en ese
                    momento secretario de Ejecución Penal de San Martín- entrevistaron a alrededor de 100 presos.
                    Confirmaron que a Patricio lo habían matado a golpes y no se había suicidado.<br />
                    El primer fiscal que intervino, Carlos Insaurralde, fue separado por seguir la versión oficial,
                    entre otras irregularidades.<br />
                    El reclamo de justicia lo encabezó una de les siete hermanes del joven, Lorena Barros Cisneros.
                    Durante un mes, su familia organizó cortes en la autopista del Buen Ayre. Tuvieron el acompañamiento
                    de la CPM y el CELS los representó en la causa judicial. En abril de 2015 el Tribunal Oral en lo
                    Criminal 4 de San Martín condenó a perpetua a los agentes Rodrigo Emilio Chaparro, Gerardo Rodolfo
                    Luna, Juan Manuel Liberto, Miguel Víctor Gallego y Héctor Aníbal Mario. El único absuelto fue el
                    agente Claudio Keem. Tanto les abogades querellantes como la fiscal Paula Leiva habían pedido
                    perpetua para los acusados. Hubo dos penitenciarios que no llegaron a ser juzgados: Víctor Silva,
                    quien apareció ahorcado en su celda de la Alcaidía de Avellaneda. Y César Raúl Benítez que escapó:
                    participó de la golpiza pero se profugó y no estuvo en el juicio.<br />
                    “La privación de la libertad no puede implicar prácticas de violencia, ni tratos indignos o
                    degradantes hacia los detenidos”, dijeron los magistrados en sus argumentos. Además, el TOC 4 pidió
                    que se investigara si había habido encubrimiento por parte del personal jerárquico.<br />
                    En 2019 se avanzó en esa línea en un juicio histórico: el Tribunal Oral Criminal 5 de San Martín
                    condenó a 6 años de prisión a Daniel Lucas Fernández, Eduardo Orsuza y Favio Ferreyra; y a 5 años a
                    Eduardo Orfanelli, Hernán Pablo Maciel y Mauricio Darío Pinedo. Todos fueron encontrados
                    responsables del delito de encubrimiento agravado. “Los actos posteriores desplegados para convencer
                    a los testigos presenciales del hecho de que mantuvieran la versión falsa a cambio de obtener
                    determinados beneficios, formaron parte de conductas propias de una estructura de poder que confió
                    en la impunidad de sus actos”, dice el fallo.<br />
                    “Para nuestra familia hoy está pendiente que se hagan efectivas las penas hacia los jefes
                    penitenciarios”, dice Lorena hoy. Su rol fue determinante en la búsqueda de justicia.

