                <h2 class="bold-font">Jonatan Herrera</h2>
                <p class="info light-font">“Los primeros ocho meses sin Joni nos encerramos en un profundo
                    dolor y no
                    hacíamos más que recordarlo”, dice hoy Julieta Riquelme, la hermana de Jonatan Herrera.<br />
                    El 4 de enero de 2015, el muchacho lavaba el auto en la vereda de su casa con su primo y uno de sus
                    hermanos en el barrio Tablada, en la zona sur de la ciudad de Rosario, cuando tres balas policiales
                    le atravesaron el cuerpo y lo mataron. Tenía 22 años. El parte forense del Instituto Médico Legal
                    indicó que los tiros le impactaron en la cabeza, el pie y en el muslo. La balacera fue consecuencia
                    de una persecución en la que intervinieron agentes de la Policía de Acción Táctica (PAT) y del
                    Comando Radioeléctrico (CRE).<br />
                    La persona a quien los agentes perseguían ya había sido aprehendida cuando el tiroteo se desató
                    contra Jonatan y los otros dos muchachos que lo acompañaban. Mientras el joven agonizaba en el
                    suelo, vecinos y vecinas vieron cómo agentes del CRE internaron plantar un arma primero a él y
                    después al chico que estaban persiguiendo.<br />
                    Sus familiares empezaron a pedir ayuda a las organizaciones sociales, políticas y de derechos
                    humanos cuando vieron que la investigación no avanzaba. Fue ahí que formaron la Multisectorial
                    Justicia por Jonathan Herrera que reunió a organizaciones sociales, políticas y académicas.
                    “Buscábamos que se esclarezca el caso y que los asesinos cumplan con una condena justa. Encaramos
                    una larga lucha y lo logramos. Aunque la noticia había salido en los diarios, en todos lados,
                    necesitábamos salir a las calles”, recuerda Julieta. Ella hoy conforma la Multisectorial contra la
                    violencia institucional de Rosario que nuclea a familiares de víctimas.<br />
                    En el proceso judicial la familia se encontró, primero, con obstáculos para llegar al debate oral y
                    público. Todavía hoy esperan respuesta de la Corte Suprema de Santa Fe por un recurso que
                    presentaron. Si bien los agentes fueron juzgados, no hay nadie detenido por el crimen.<br />
                    Al comienzo de la causa, cuatro policías de la PAT fueron imputados, uno de ellos por el delito de
                    homicidio y tres por tentativa de homicidio. La familia tuvo que pelear contra la posibilidad de un
                    juicio abreviado que alivianara los castigos y clausurara la posibilidad de saber qué pasó ese día.
                    El juicio abreviado implicaba modificar la calificación legal inicial de “tentativa de homicidio” a
                    una menos gravosa: “abuso de armas”.<br />
                    Les familiares de Jonatan se acercaron al Ministerio Público de la Defensa y consiguieron nuevas
                    abogadas que les representaran y así avanzar en el debate oral y público. El 6 de abril de 2017 un
                    tribunal integrado por los jueces Juan Carlos Curto, Rodolfo Zvala y Juan José Alarcón absolvió a la
                    agente Gladys Galindo, policía del Comando Radioeléctrico de 37 años, por falta de pruebas. Y
                    condenó a Ramiro Rosales, agente de la PAT, a 6 años y 6 meses de prisión e inhabilitación por
                    homicidio culposo. En tanto, la pena para Luis Sosa (de 23 años y policía de la PAT) y Jonatan
                    Alejandro Gálvez (agente de la PAT de 22 años) fue de 3 años y 8 meses por abuso de arma. Por
                    último, Francisco Rodríguez (agente de la PAT de 21 años) fue condenado a 3 años de prisión por el
                    mismo delito que los anteriores.<br />
                    En mayo de 2019 Ramiro Rosales, el único policía que estaba preso por el crimen, consiguió la
                    libertad. Las pericias acreditaron que su arma disparó uno de los dos balazos letales. El efectivo
                    de 26 años recién ingresaba a la PAT tras recibir una breve instrucción de seis meses. La fiscalía y
                    la familia, a través de la querella, presentaron un recurso de inconstitucionalidad ante la Corte
                    Suprema provincial que todavía está pendiente. La hermana de Jonatan, en el barrio Tablada, hoy
                    trabaja para tener un espacio propio que lleve el nombre del chico.

