                <h2 class="bold-font">Alan Tapia</h2>
                <p class="info light-font">A Alan Stefano Tapia le decían “Mono”, era estudiante y profesor
                    de tango.
                    Tenía 19 años y participaba en el grupo Colectivo Militante. Estaba acostado en su cama, en la casa
                    278 del Barrio Mitre, en Saavedra, al norte de la Ciudad de Buenos Aires, cuando agentes del Grupo
                    Especial de Operaciones Federales (GEOF) y de la División Homicidios de la Policía Federal
                    derribaron a patadas la puerta de entrada. Vivía con su madre, su padre, sus abuelos y dos hermanos.
                    Pablo Valente, un agente entrenado para operaciones antiterroristas y rescates de rehenes, le
                    disparó en el estómago. Alan apenas atinó a sentarse en la cama. Fue en la madrugada del 15 de
                    febrero de 2012 durante un allanamiento que tenía como objetivo buscar y detener a un menor de edad:
                    el Poder Judicial consideró que era conveniente desplegar al GEOF porque se trataba de un “barrio
                    peligroso”. La orden para el allanamiento la había pedido el fiscal José María Campagnoli.<br />
                    Mientras Alan agonizaba, su familia estaba maniatada y precintada en la planta alta de la casa.
                    Preguntaban una y otra vez sobre lo que pasaba abajo y los policías le respondían que “todo estaba
                    bien”. Cuando supieron que el joven estaba herido, intentaron sacar a los policías de la casa. El
                    allanamiento terminó con el padre y los hermanos de Alan detenidos por “resistencia a la
                    autoridad”.<br />
                    Alan esperó más de cuarenta minutos por atención médica, hasta que llegó una ambulancia que lo
                    trasladó al Hospital Pirovano, donde murió, a las 11 de la mañana.<br />
                    Pablo Valente, el agente que lo mató, admitió haber disparado porque, según él, pensó que el chico
                    estaba armado. Cuando se enteró que estaba procesado, Valente escapó y estuvo prófugo dos semanas.
                    En mayo de 2012 ingresó detenido al penal de Marcos Paz.<br />
                    “El primer acompañamiento que tuvimos fue el de Colectivo Militante que era donde militaban mi
                    hermano y mi prima Karina. Enseguida organizaron una marcha a la fiscalía al día siguiente y una
                    reunión en el CELS. Ellos conocían de este organismo y ahí me explicaron todo lo que yo no sabía:
                    que el policía ya estaba identificado y el Ministerio de Seguridad lo iba a poner a disposición”,
                    recuerda Milton Tapia, el hermano de Alan, años después.<br />
                    El asesinato de Alan llegó a juicio en 2013. El policía invocó “legítima defensa”. Dijo que estaba
                    oscuro, que la víctima lo había amenazado con un “arma tumbera” y por eso abrió fuego. El fiscal
                    Eduardo Marazzi decidió no acusar, justificó los hechos por tratarse de un barrio “peligroso” y los
                    jueces siguieron esa misma línea: los integrantes del Tribunal Oral en lo Criminal 11 -con el voto
                    en disidencia del juez Alberto Huarte- absolvieron a Valente. Tras el veredicto la por entonces
                    Procuradora General de la Nación, Alejandra Gils Carbó separó al fiscal de su cargo y consideró que
                    la sentencia absolutoria condujo a que "todos los vecinos del barrio Mitre se quedaran con la idea
                    de que un policía puede entrar a sus casas y matarlos”.<br />
                    Claudia Riveros, la mamá de Alan, como tantos otros familiares, se convirtió en una referente en
                    materia de violencia institucional, insistió con la búsqueda de justicia y ayudó a otras víctimas
                    del barrio. En abril de 2015, la fiscal a cargo de la Fiscalía General N°1 ante la Cámara Nacional
                    de Casación Penal, Gabriela Baigún, y el, por entonces, titular de la Procuraduría de Violencia
                    Institucional (Procuvin), Abel Córdoba, la acompañaron en una presentación contra la sentencia
                    absolutoria. Exigían que se revisara el fallo.<br />
                    En 2016, la mamá de Alan falleció. Dos años después, la Sala I de Casación Penal, con el voto del
                    juez Gustavo Hornos al que adhirió su colega Mariano Borinsky, confirmó la sentencia que había
                    rechazado la acusación contra Valente por exceso de legítima defensa. "En atención al contexto
                    fáctico y jurídico en el que se desarrolló el suceso de autos, era lógico pensar en la milésima de
                    tiempo en el que aconteció, que se trataba de un arma de fuego y por ende que podía ser disparada",
                    analizaron los jueces del voto mayoritario.<br />
                    El crimen de Alan Stefano Tapia, “El Mono”, continúa impune pero su memoria sigue vigente en las
                    paredes del Barrio Mitre que reclaman justicia por él.

