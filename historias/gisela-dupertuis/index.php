                <h2 class="bold-font">Gisela Dupertuis</h2>
                <p class="info light-font">“Quisieron tratarlo como un suicidio”. Es lo primero que dice
                    Ana, la sobrina
                    de Gisela Dupertuis, cuando recuerda el femicidio de su tía. A casi dos años del crimen, ella y su
                    familia, oriundes de la provincia del Chaco, todavía esperan que haya justicia por el asesinato de
                    la joven policía bonaerense que trabajaba en el comando de patrullas de Ezeiza. Aunque la expareja
                    de la mujer está detenido, el proceso judicial no avanza con la celeridad que esperan. Desde un
                    comienzo se encontraron con trabas para conocer la verdad, por lo que recurrieron a medios locales,
                    chaqueños y nacionales para poder desarmar, así, la trama de encubrimiento que intentaba exculpar al
                    policía que la mató.<br />
                    El 26 de diciembre de 2018 encontraron a Gisela, de 32 años, sobre su cama con un tiro en la cabeza.
                    Fue en la casa que compartía desde hacía poco más de un año con su pareja, Jonathan Giuliano, en
                    pleno centro del municipio de Ezeiza. El hombre, agente municipal, contó su propia versión de los
                    hechos: dijo que Gisela entró en crisis tras una discusión y se disparó a sí misma. Esta ficción se
                    repite en otras historias de femicidio que intentan ser narradas como suicidios por parte de los
                    propios varones que gatillan, prenden fuego o golpean hasta matar a las mujeres con las que
                    conviven.<br />
                    Cuando la familia de Gisela llegó a la casa, les repitieron la mentira: que se había suicidado. Y el
                    Poder Judicial rápidamente, sin dudar, caratuló la investigación como "averiguación de muerte".<br/>
                    “Esa noche fue todo un caos porque había un montón de funcionarios, más que nada jefes que
                    estuvieron antes que supiéramos que ella estaba muerta”, recuerda Ana.<br />
                    Desde un primer momento, sus familiares dudaron de esa versión oficial: en la habitación había más
                    de un disparo porque encontraron 5 vainas servidas en el suelo y las huellas de una pelea. De a poco
                    pudieron reconstruir la historia de maltratos y la trama de violencia machista de la cual Gisela era
                    víctima. Por la presión de la familia, que visibilizó el caso a través de los medios de
                    comunicación, y el abogado que tomó la causa ampliaron la pericia balística inicial y confirmaron
                    que lo que había pasado en el lugar había sido un crimen y no un suicidio. La causa cambió a
                    "homicidio agravado por vínculo" e intervino la UFI N°2 de Ezeiza, que pidió que se investigara al
                    principal acusado. Recién el 14 de febrero del 2019 ordenaron la detención del hombre que estuvo
                    prófugo durante un día.<br />
                    “Logramos que sea nombrada como lo que fue: un femicidio”, dice Ana. La última vez que su familia la
                    vio con vida fue en el almuerzo de navidad. No tienen dudas que la policía bonaerense ayudó a
                    Jonathan Giuliano a encubrir el crimen que había cometido. Por eso quieren que se investigue la
                    violencia institucional en la causa.<br />
                    De acuerdo al Informe de Femicidios de la Justicia Argentina que elabora todos los años la Oficina
                    de la Mujer (OM) de la Corte Suprema de Justicia de la Nación en 2018 hubo 278 víctimas letales de
                    violencia de género. Entre estos crímenes contabilizaron uno en manos de un policía local, se trata
                    del femicidio de Gisela.<br />
                    Según los registros del CELS, casi todas las mujeres policías asesinadas en femicidios sufrieron
                    impactos con un arma de fuego, en varios casos con el arma reglamentaria provista por una
                    institución de seguridad, como el arma con la que Giuliano, según la investigación, le disparó a
                    Gisela.</p>
