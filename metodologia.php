        <h2 class="red-title medium-font">metodología de la base</h2>
        <p class="read-more info light-font mt-4">
            Desde 1996 registramos en una base de datos los hechos de violencia en los que participan funcionaries de
            agencias estatales y privadas de seguridad. <br/>Recolectamos las publicaciones periodísticas sobre las muertes y
            lesiones causadas por integrantes de las fuerzas de seguridad y sobre las muertes de funcionaries y las
            lesiones que reciben. De esta forma, la información recolectada refiere a tres unidades de análisis: el
            hecho, les funcionaries involucrades y las víctimas. Pese a los posibles sesgos en la información, el
            sostenimiento de la metodología durante más de dos décadas permite presentar series y observar tendencias. <br/>
            Para la construcción de esta base de datos utilizamos como fuente los periódicos de mayor circulación:
            Clarín, La Nación, Página/12 y Crónica. En forma complementaria incorporamos datos provenientes de diarios
            locales y causas judiciales. Realizamos un seguimiento diacrónico de los casos si aparecen noticias como,
            por ejemplo, la sentencia de un juicio. Procesamos los datos con SPSS Statistics y almacenamos copias de las
            noticias en formato PDF.<br />
            Debido a sus fuentes de origen, los datos primarios son ilustrativos pero no exhaustivos porque la
            publicidad de los hechos está sujeta a decisiones periodísticas. Es posible que ocurra un subregistro de
            hechos violentos de menor gravedad que incida en una subcuantificación de les herides. A su vez, es
            esperable que siempre que la víctima sea un funcionarie la noticia aparezca y no así cuando no lo es. Lo
            mismo ocurre con la ubicación geográfica de los hechos: es más probable que sean publicados si ocurren en el
            AMBA. Finalmente, las torturas y los apremios ilegales rara vez son considerados publicables, salvo cuando
            involucran la desaparición, la muerte o lesiones graves. En el mismo sentido, la base de datos se actualiza
            con información posterior que también depende de los criterios periodísticos. Por ejemplo, si no se publica
            la muerte de alguien que había sido heride de gravedad se genera un sesgo ya que las muertes de funcionaries
            tienen mayor difusión. Por último, la generación de noticias periodísticas parte, en muchos casos, de los
            informes policiales, lo que puede generar que el relato inicial sea la versión de la fuerza de seguridad.
            Además, en algunos casos no se cuenta con la información completa de las víctimas particulares o de les
            funcionaries.
