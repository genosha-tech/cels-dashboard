        <h1 class="section-title bold-font">5. bajo custodia</h1>
        <p class="info light-font mt-4">Las dependencias policiales no son establecimientos preparados para alojar personas
            por tiempo prolongado, ya que no cuentan con los espacios y medidas de seguridad básicas. Sin embargo, su
            uso como lugar de detención por semanas y meses es frecuente en muchos lugares del país. Allí, la violencia
            sistemática, el hacinamiento y la precariedad de la vida son un problema estructural de larga data. En
            general, las policías tampoco cuentan con lineamientos claros para gestionar espacios de encierro.<br/>
            Cuando los hechos de violencia ocurren, quienes trabajan en las comisarías tienen la capacidad para
            controlar tanto la escena del hecho en sus primeros momentos como a les detenides que fueron testigues. En
            muchas ocasiones intentan anular esos testimonios con amenazas o negociaciones a cambio de mejoras en las
            condiciones de detención de quienes están privades de la libertad. Eso acarrea serias dificultades para
            investigar estos casos.
        </p>
