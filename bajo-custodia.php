                <p class="block-title medium-font mt-3 mb-0">Matías Iberra</p>
                <p class="case-subtitle medium-font">1/7/2019, Merlo, Buenos Aires</p>
                <p class="info light-font">La noche del 26 de junio de 2019 Matías Iberra, un joven en
                    situación de
                    calle, fue increpado por dos personas en una plaza del centro de la localidad de Merlo, provincia de
                    Buenos Aires. Lo acusaban de haber hurtado, junto con otras personas, un equipo de mate. Los
                    denunciantes le sacaron su mochila y se dirigieron a la Comisaría Primera de la zona. Matías los
                    siguió hasta la dependencia policial. La policía no encontró entre sus pertenencias los elementos
                    supuestamente hurtados. A pesar de ello, lo demoraron, el fiscal pidió su detención y el juez la
                    convalidó. Le dictaron la prisión preventiva por no tener domicilio fijo. Quedó preso en la
                    comisaría. Cinco días después, el 1 de julio, Matías apareció muerto en otra comisaría, la Segunda
                    de Merlo, a la que había sido trasladado algunas horas antes. Estaba golpeado. Los efectivos
                    policiales de la Comisaría Segunda dejaron constancia de que al recibirlo presentaba rastros de
                    golpes. Sin embargo, un certificado médico efectuado unas horas antes decía que no tenía lesiones
                    visibles. No se alertó a ninguna autoridad de lo ocurrido, ni de las inconsistencias de la
                    documentación. Las comisarías primera y segunda de Merlo fueron allanadas recién el 5 de julio,
                    cuatro días después de los hechos. La investigación para determinar qué sucedió en la Comisaría
                    Primera y por qué los policías de esa dependencia decidieron llevarlo a otra comisaría sigue en
                    curso.</p>
