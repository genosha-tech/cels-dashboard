        <h1 class="section-title bold-font">3. femicidios policiales</h1>
        <p class="info light-font mt-4 mb-md-5">Los femicidios cometidos por funcionarios de las fuerzas de seguridad
            son
            una parte
            importante de las muertes de mujeres en hechos de violencia institucional. Entre 2010 y 2020 al menos 48
            mujeres fueron asesinadas en el AMBA por sus parejas o exparejas policías, 17 eran funcionarias de
            seguridad.<br />
            La portación del arma reglamentaria las 24 horas, amparada en el “estado policial”, pone en riesgo a las
            mujeres que conviven con funcionarios de seguridad: en los casos en los que existe violencia machista él
            puede utilizar el arma para hostigar, amenazar, herir o matar. En algunas policías la normativa restringe la
            portación si el funcionario fue denunciado por violencia machista, pero muchas veces no existe una denuncia
            previa formal. <br />
            Como ocurre en muchos otros casos de uso ilegal de la fuerza policial, existen prácticas de encubrimiento de
            las muertes cuyos responsables integran las fuerzas de seguridad. Como auxiliares de la justicia, las
            policías controlan los primeros momentos de la escena del hecho, lo que significa que pueden falsear el
            relato de lo sucedido y manipular la escena para hacerla coincidir con su versión.<br />
            En muchos casos las mujeres víctimas también son integrantes de las fuerzas de seguridad, lo que muestra un
            traslado hacia la vida privada de los patrones de discriminación, acoso laboral y violencia de género que
            están extendidos al interior de las policías.<br />
            En los últimos años, la creciente incorporación de mujeres a las fuerzas de seguridad y la visibilización
            social de la violencia machista han llevado en distintas jurisdicciones a la creación de oficinas cuya
            misión específica es atender esta problemática.
        </p>
