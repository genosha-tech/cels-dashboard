        <h1 class="section-title bold-font">6. represión con armas “menos letales”</h1>
        <p class="info light-font mt-4">
            En los últimos años, se expandió en todo el mundo el uso de un tipo de armamento al que se llama “menos
            letal” o “no letal” como las armas de fuego que disparan postas de goma y las pistolas Taser. Argentina no
            es la excepción. Ya en 2007 la muerte del maestro Carlos Fuentealba visibilizó este problema: fue asesinado
            por la policía de Neuquén que le disparó una granada de gas lacrimógeno a muy corta distancia. Estas armas
            se comercializan alegando que minimizan los riesgos de letalidad y de heridas graves. Sin embargo, está
            comprobado que producen muertes, discapacidades y lesiones permanentes. Los disparos a corta distancia o en
            la cabeza y el rostro con balas de goma son especialmente peligrosos. Aunque los principios internacionales
            sobre el uso de la fuerza deben aplicarse también a estas armas, son repetidos los casos de uso
            indiscriminado que violan estos criterios.<br />
            Existe poca información producida por el Estado sobre las consecuencias del uso de estas armas, y menos aún
            lineamientos sobre su utilización segura, lo que deja librados los criterios a los pocos datos que proveen
            los fabricantes. En algunos casos, las empresas que fabrican postas de goma establecen que un disparo a
            menos de diez metros de distancia puede matar.<br />
            Presentar a este tipo de armas como inocuas conduce a que las fuerzas de seguridad interpreten que están
            habilitadas a utilizarlas con menos restricciones que las armas de fuego con munición letal. La falta de
            mecanismos de rendición de cuentas y de instancias de formación y evaluación profundizan el problema.
        </p>
