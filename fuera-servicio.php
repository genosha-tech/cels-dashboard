        <h1 class="section-title bold-font">7. fuera de servicio</h1>
        <p class="info light-font mt-4">El uso de armas de fuego por parte de funcionaries de seguridad mientras están fuera
            de servicio es uno de los problemas principales del uso de la fuerza estatal: la mayoría de las muertes
            causadas por funcionaries de seguridad y también la mayoría de las muertes violentas de les funcionaries
            ocurren en esta circunstancia.<br />
            El centro de esta cuestión es el “estado policial”, una disposición legal que habilita a quienes integran o
            integraron las fuerzas de seguridad a portar el arma reglamentaria las 24 horas. En muchas policías no es
            una obligación llevar el arma, pero les funcionaries de casi todas las fuerzas de seguridad nacionales y
            provinciales lo hacen porque sigue vigente la idea que asocia la esencia del “ser policía” con portar el
            arma de fuego. Además, muchas dependencias policiales no tienen lugares específicos y protegidos para dejar
            las armas.
            <br />
            La portación del arma fuera del horario de servicio confluye con la creencia de que les efectives de
            seguridad deben intervenir en cualquier circunstancia. Con frecuencia, portan el arma con el cartucho en la
            recámara y el martillo bajo, como lo permiten las regulaciones de muchas policías, y en situaciones
            conflictivas o delictivas pueden abrir fuego con solo apretar la cola del disparador. De esto deriva uno de
            los patrones principales de la violencia policial. Esta costumbre es un peligro para la vida de les
            particulares y de les funcionaries, incluso de quienes ya se retiraron.
            <br />
            En otras ocasiones, les funcionaries usan el arma otorgada por el Estado para resolver conflictos personales
            y entonces situaciones de la vida cotidiana, como una discusión de tránsito o un conflicto entre vecines, se
            transforman en escenarios para intervenciones potencialmente letales. La portación del arma fuera del
            horario laboral promueve además el desplazamiento de la violencia hacia los lugares de residencia de quienes
            forman parte de las fuerzas de seguridad: sus hogares y sus barrios.
        </p>
