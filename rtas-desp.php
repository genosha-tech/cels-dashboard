        <h1 class="section-title bold-font">4. ejecuciones policiales y uso desproporcionado de la fuerza letal</h1>
        <p class="info light-font mt-4 mb-md-5">Muchas veces las intervenciones policiales terminan en ejecuciones o en el
            uso
            desproporcionado de la fuerza dado que la situación no habilitaba utilizar el arma de fuego. Y producen
            muertes y heridas evitables.<br/>
            Entre las situaciones en las que estos hechos de violencia son más frecuentes están las persecuciones,
            muchas veces originadas en que alguien quiso eludir un control vehicular. También el uso de la fuerza en
            protestas sociales o desalojos deriva en muchas ocasiones en intervenciones violentas. En otros casos, en
            acciones de prevención del delito, flagrancia u operativos de investigación criminal como allanamientos o
            detenciones, las intervenciones son desproporcionadas en relación al fin que persiguen: el hecho podría
            ameritar que les policías intervengan para evitar un delito o detener a quienes lo cometieron pero terminan
            causando un daño mucho mayor que el que pretendían evitar. Muchos de estos despliegues policiales tienen
            como origen la persecución de un delito menor y terminan causando la muerte o daños graves a la salud.<br/>
            Les integrantes de las fuerzas de seguridad solo pueden usar la fuerza con resultados potencialmente letales
            de manera excepcional y en última instancia, es decir frente a una amenaza inminente a la propia vida o a la
            de otres. Las normativas nacionales e internacionales establecen los principios de necesidad, racionalidad,
            gradualidad y proporcionalidad en el uso de la fuerza e indican que si la situación no amerita el uso del
            arma de fuego se deben utilizar otras formas de intervención. <br/>Toda actuación por fuera de estos principios
            es ilegítima, aunque ocurra en el marco de procedimientos a primera vista legales. <br/>
            La intervención con extrema violencia en situaciones conflictivas, especialmente cuando se trata de barrios
            pobres, se reitera con una frecuencia preocupante. La ausencia habitual de funcionaries judiciales
            supervisando los operativos que ordenaron y la falta de procedimientos rigurosos de registro -como por
            ejemplo, la grabación audiovisual exhaustiva- favorecen estos hechos de violencia.
        </p>
